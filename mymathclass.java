import java.lang.Math;
public class mymathclass{
    public static void main(String[] args) {
       
        double idk1 = Math.floor(10);
        double idk2 = Math.floor(7);
        System.out.println("Math.floor: " + idk1) ;
        System.out.println("Math.floor:" + idk2) ;

        double sorry = Math.ceil(6.251);
        System.out.println("Math.ceil: " + sorry);
        
        
        int na = Math.min(17, 41);
    
        System.out.println("Math.min: " + na);
        
        
        double po = Math.pow(6, 2);
        System.out.println("Math.pow: " + po);
        
        
        int sir = Math.abs(-53);
        System.out.println("Math.abs:" + sir);
        
        
        long bat_ang_dami = Math.round(21.42);
        System.out.println("Math.round: " + bat_ang_dami);
    }
}