public class O4LaboratoryExercise {
    public static void main(String[] args) {
        int[] myArr = { 18, 26, 33 };
        int sum = 0;
        for (int x = 0; x < myArr.length; x++) {
            sum += myArr[x];
        }
        System.out.println("The Sum Is " + sum); // write the concatenated string value
    }
}

